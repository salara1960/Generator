void OLED_StartScroll(uint8_t start, uint8_t stop, bool left)
{
uint8_t dir;
    if (left) dir = 0x27; else dir = 0x26;
	uint8_t dat[] = {
		0x2E,     // Deactivate any previous scroll
		dir,      // Horizontal Scroll Setup
		0x00,     // Dummy byte
		start,    // Start page address
		0x00,     // Scroll speed (e.g., 5 frames)
		stop,     // Stop page address
		0x00,     // Dummy byte
		0xFF,     // Dummy byte
		0x2F      // Activate scroll
	};

	I2C_WriteDatas(iCmd, dat, sizeof(dat));

    scrollFlag = true;
}
//------------------------------------------------------------------------------
void OLED_StopScroll(void)
{
    I2C_WriteData(1, 0x2E);     // Deactivate scroll

    scrollFlag = false;
}
