void OLED_text_xy(const char *stroka, uint8_t cx, uint8_t cy, bool inv)
{
uint8_t i, lin = cy - 1, col = cx - 1;
int len = strlen(stroka);
uint8_t dat[] = {0, 0x10, 0};
uint8_t cif[] = {0, 0, 0, 0, 0, 0, 0, 0};
uint8_t first[] = {
	OLED_CMD_SET_COLUMN_RANGE,
	col << 3,
	0x7f,
	OLED_CMD_SET_PAGE_RANGE,
	lin,
	7
};

	I2C_WriteDatas(iCmd, first, sizeof(first));

	for (i = 0; i < len; i++) {
		if (stroka[i] == '\n') {
			dat[2] = 0xB0 | ++lin;
			I2C_WriteDatas(iCmd, dat, sizeof(dat));
		} else {
			memcpy(cif, &font8x8[(uint8_t)stroka[i]][0], 8);
			if (inv) for (uint8_t j = 1; j < sizeof(cif); j++) cif[j] = ~cif[j];
			I2C_WriteDatas(iDat, cif, sizeof(cif));
		}
	}
}
