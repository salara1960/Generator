//------------------------------------------------------------------------------
void I2C_WriteData(uint8_t cmd, uint8_t data)
{
#ifdef SET_SSD1306    
uint8_t mode = OLED_CONTROL_BYTE_DATA_STREAM;
    if (cmd) mode = OLED_CONTROL_BYTE_CMD_STREAM;

    I2C_GenerateSTART(i2cDev, ENABLE);
    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_MODE_SELECT));
    I2C_Send7bitAddress(i2cDev, OLED_I2C_ADDR << 1, I2C_Direction_Transmitter);

    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED));
    I2C_SendData(i2cDev, mode);    // Data/Cmd mode

    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_BYTE_TRANSMITTED));
    I2C_SendData(i2cDev, data);
    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_BYTE_TRANSMITTED));
    I2C_GenerateSTOP(i2cDev, ENABLE);
#else
    SSD1306_SELECT();

        if (cmd) SSD1306_DC_CMD();
            else SSD1306_DC_DATA();

        SPI_I2S_SendData(SPI1, data);
    
    SSD1306_UNSELECT();
#endif
}
//------------------------------------------------------------------------------
void I2C_WriteDatas(uint8_t cmd, uint8_t *datas, int len)
{
#ifdef SET_SSD1306
uint8_t mode = OLED_CONTROL_BYTE_DATA_STREAM;
    if (cmd) mode = OLED_CONTROL_BYTE_CMD_STREAM; 

    I2C_GenerateSTART(i2cDev, ENABLE);
    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_MODE_SELECT));
    I2C_Send7bitAddress(i2cDev, OLED_I2C_ADDR << 1, I2C_Direction_Transmitter);

    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED));
    I2C_SendData(i2cDev, mode);    // Data/Cmd mode

    while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_BYTE_TRANSMITTED));
    for (int i = 0; i < len; i++) {
    	I2C_SendData(i2cDev, *datas++);
    	while (!I2C_CheckEvent(i2cDev, I2C_EVENT_MASTER_BYTE_TRANSMITTED));
    }
    I2C_GenerateSTOP(i2cDev, ENABLE);
#else
    SSD1306_SELECT();//GPIO_ResetBits(GPIOA, CS_Pin);

        if (cmd) SSD1306_DC_CMD(); //GPIO_ResetBits(GPIOA, DC_Pin);
            else SSD1306_DC_DATA(); //GPIO_SetBits(GPIOA, DC_Pin);
        
        while (len--) SPI_I2S_SendData(SPI1, *(uint8_t *)datas++);

    SSD1306_UNSELECT();//GPIO_SetBits(GPIOA, CS_Pin);
#endif
}
