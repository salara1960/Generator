//------------------------------------------------------------------------------
void OLED_clear_line(uint8_t cy, bool inv)
{
cy--;
uint8_t first[] = {
	OLED_CMD_SET_COLUMN_RANGE,
	0,
	0x7f,
	OLED_CMD_SET_PAGE_RANGE,
	cy,
	7,
	0xB0 | cy
};

    buffer = (char *)calloc(1, OLED_WIDTH);
    if (buffer) { 
        if (inv) memset(buffer, 0xff, OLED_WIDTH);

	    I2C_WriteDatas(iCmd, first, sizeof(first));
	    I2C_WriteDatas(iDat, buffer, OLED_WIDTH);

        free(buffer);
    }

	Delay_Ms(1);
}
